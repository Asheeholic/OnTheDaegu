package co.yedam.otd.rental.vo;

public class BicycleVO {
	private int bicycleNo;
	private String bicycleError;
	private String bicycleLost;
	private String bicyclePossible;
	private String bicycleLat;
	private String bicycleLng;
	private String parkNumber;

	public int getBicycleNo() {
		return bicycleNo;
	}

	public void setBicycleNo(int bicycleNo) {
		this.bicycleNo = bicycleNo;
	}

	public String getBicycleError() {
		return bicycleError;
	}

	public void setBicycleError(String bicycleError) {
		this.bicycleError = bicycleError;
	}

	public String getBicycleLost() {
		return bicycleLost;
	}

	public void setBicycleLost(String bicycleLost) {
		this.bicycleLost = bicycleLost;
	}

	public String getBicyclePossible() {
		return bicyclePossible;
	}

	public void setBicyclePossible(String bicyclePossible) {
		this.bicyclePossible = bicyclePossible;
	}

	public String getBicycleLat() {
		return bicycleLat;
	}

	public void setBicycleLat(String bicycleLat) {
		this.bicycleLat = bicycleLat;
	}

	public String getBicycleLng() {
		return bicycleLng;
	}

	public void setBicycleLng(String bicycleLng) {
		this.bicycleLng = bicycleLng;
	}

	public String getParkNumber() {
		return parkNumber;
	}

	public void setParkNumber(String parkNumber) {
		this.parkNumber = parkNumber;
	}

}
